class Card {
  final String imageUrl;
  final String title;
  final String text;

  Card({this.imageUrl, this.title, this.text});
}

class Link {
  final String text;
  final String url;

  Link({this.text, this.url});
}

class Footer {
  final String title;
  final String description;

  Footer({this.title, this.description});
}